#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*to_del;
	t_list	*current;

	if (!lst || !del)
		return ;
	current = *lst;
	while (current)
	{
		to_del = current;
		current = current->next;
		del(to_del->content);
		free(to_del);
	}
	*lst = NULL;
}
